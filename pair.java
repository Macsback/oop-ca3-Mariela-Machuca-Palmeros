public class pair {


        public int row;
        public int col;

        pair(int row, int col)
        {
            this.row = row;
            this.col = col;
        }

    pair()
    {
        this.row = 0;
        this.col = 0;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    public void setCol(int col) {
        this.col = col;
    }

}

